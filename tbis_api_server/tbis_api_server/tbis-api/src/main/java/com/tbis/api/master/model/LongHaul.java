package com.tbis.api.master.model;


public class LongHaul {	
	private int totalRequested;
	private int totalAlloted;
	private int totalTrips;
	private int totalDelays;
	private int totalAbnormalities;
	private int totalTats;
	private int totalCustomers;
	
	public int getTotalRequested() {
		return totalRequested;
	}
	public void setTotalRequested(int totalRequested) {
		this.totalRequested = totalRequested;
	}
	public int getTotalAlloted() {
		return totalAlloted;
	}
	public void setTotalAlloted(int totalAlloted) {
		this.totalAlloted = totalAlloted;
	}
	public int getTotalTrips() {
		return totalTrips;
	}
	public void setTotalTrips(int totalTrips) {
		this.totalTrips = totalTrips;
	}
	public int getTotalDelays() {
		return totalDelays;
	}
	public void setTotalDelays(int totalDelays) {
		this.totalDelays = totalDelays;
	}
	public int getTotalAbnormalities() {
		return totalAbnormalities;
	}
	public void setTotalAbnormalities(int totalAbnormalities) {
		this.totalAbnormalities = totalAbnormalities;
	}
	public int getTotalTats() {
		return totalTats;
	}
	public void setTotalTats(int totalTats) {
		this.totalTats = totalTats;
	}
	public int getTotalCustomers() {
		return totalCustomers;
	}
	public void setTotalCustomers(int totalCustomers) {
		this.totalCustomers = totalCustomers;
	}
	
		
}


