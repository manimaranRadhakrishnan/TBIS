package com.tbis.api.master.model;

public class CardMaster {	
	private int cardId;
	private String cardName;
	private int cardDataLength;
	private String partNoStart;
	private String vendorStart;
	private String userLocationStart;
	private String qtyStart;
	private String slNoStart;
	private int userId;
	
	public int getCardId() {
		return cardId;
	}
	public void setCardId(int cardId) {
		this.cardId = cardId;
	}
	public String getCardName() {
		return cardName;
	}
	public void setCardName(String cardName) {
		this.cardName = cardName;
	}
	public int getCardDataLength() {
		return cardDataLength;
	}
	public void setCardDataLength(int cardDataLength) {
		this.cardDataLength = cardDataLength;
	}
	public String getPartNoStart() {
		return partNoStart;
	}
	public void setPartNoStart(String partNoStart) {
		this.partNoStart = partNoStart;
	}
	public String getVendorStart() {
		return vendorStart;
	}
	public void setVendorStart(String vendorStart) {
		this.vendorStart = vendorStart;
	}
	public String getUserLocationStart() {
		return userLocationStart;
	}
	public void setUserLocationStart(String userLocationStart) {
		this.userLocationStart = userLocationStart;
	}
	public String getQtyStart() {
		return qtyStart;
	}
	public void setQtyStart(String qtyStart) {
		this.qtyStart = qtyStart;
	}
	public String getSlNoStart() {
		return slNoStart;
	}
	public void setSlNoStart(String slNoStart) {
		this.slNoStart = slNoStart;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	
		
}
